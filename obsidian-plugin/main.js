    if (!this.bootstrapped) {
      for (let i = 0; i < 40; i++) {
        const page2 = await this.plugin.api.events(this.cursor, 200, EVENT_TYPES_FILTER);
        if (!page2.length) break;
        this.cursor = page2[page2.length - 1].id;
        this.pushEvents(page2);
        if (page2.length < 200) break;
      }
      this.bootstrapped = true;
      return;
    }
    const page = await this.plugin.api.events(this.cursor, 30, EVENT_TYPES_FILTER);
    if (page.length) {
      this.cursor = page[page.length - 1].id;
      this.pushEvents(page);
    }
